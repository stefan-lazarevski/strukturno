/*  Од стандарден влез се чита број x кој означува колку километри траело едно возење со такси. 
На екран треба да се испечати вкупната цена која што клиентот треба да ја плати. Првите 5км имаат фиксна цена од 80ден, 
за следните 3км се наплаќа по 20ден по километар и за сите останати километри кои ќе бидат поминати се наплаќа 25ден по километар. 
Доколку се поминати 5км или помалку од 5км тогаш крајната цена е ослободена од ДДВ, а во спротивно треба да се наплати и ДДВ од 20%.

For example:

Input	Result
5
80 denari
*/

#include <stdio.h>

int main()
{

    int x;
    float cena=0;
    scanf("%d ", &x);
    if (x <= 5) cena=80;
    if (x>5 && x<=8) cena=(80 + 20 *(x-5))*1.2;
    if (x>8) cena=(140+25*(x-8))*1.2;
    printf("%.0f denari", cena);

    return 0;
}

